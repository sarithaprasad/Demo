package com.tib.firstclass;

public class Variables {
	
	
	int y=20;
	
	static int z=40;
	
	  public static void main(String...args) {
		  
		  Variables s=new Variables();
		  
		  int x=50;
		
		System.out.println("Value of local vaiable - X  is: "+x);
		
		System.out.println("Value of instance/Object vaiable - Y  is: "+  s.y   );
		
		System.out.println("Value of local vaiable - Z  is: "+z);
		
	}
	

}
